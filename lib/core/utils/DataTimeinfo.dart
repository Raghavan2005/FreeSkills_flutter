import 'package:easy_localization/easy_localization.dart';

class Datatimeinfo {
  static String getCurrentDateTime() {
    DateTime now = DateTime.now();
    DateFormat formatter = DateFormat('yyyy-MM-dd HH:mm:ss');

    return formatter.format(now);
  }
}
